package io.yupiik.uship.configuration;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * When used with {@link Binder#bindPrefixed(Object)}, it enables to pass the prefix to use to filter system properties.
 */
@Target(TYPE)
@Retention(RUNTIME)
public @interface Prefix {
    /**
     * @return prefix used by all parameters of the configuration.
     */
    String value();
}
